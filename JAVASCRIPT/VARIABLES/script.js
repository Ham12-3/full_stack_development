// Using var keyword

var myAge = 30;

myAge = 20;

console.log(myAge); // 20

// Using let keyword

let myScore = 10;

myScore = 50;
console.log(myScore); // 50

const myGrade = "A";

// Reassign value to a constant variable

myGrade = "B";

console.log(myGrade); // Error: Assignment to constant variable.
