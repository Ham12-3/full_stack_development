// Comparison Operators in JavaScript are used to compare two values. It returns a boolean value either true or false.

let aageOfJohn = 25;

let ageOfDoe = 30;

let userInput = "25";

console.log(aageOfJohn == userInput); // true

// Using the ===

console.log(aageOfJohn === userInput); // false

// using the != operator

console.log(aageOfJohn != ageOfDoe); // true

// using the !== operator

console.log(aageOfJohn !== ageOfDoe); // true

// Using the > operator

console.log(aageOfJohn > ageOfDoe); // false

// Using the < operator

console.log(aageOfJohn < ageOfDoe); // true

// Using the >=

console.log(aageOfJohn >= ageOfDoe); // false

// using the <= operator

console.log(aageOfJohn <= ageOfDoe); // true
