// PRIMITIVE TYPES
// Number - Storing age in a variable

const age = 25;

console.log(age); // 25

// String - Storing name in a variable

const name = "Alice";

console.log(name); // Alice

// Boolean - Is the user logged in?
const isLoggedIn = true;

console.log(isLoggedIn); // true

// Undefined - A variable that has been decalred but not yet assigned a value

let numOfChildren;

console.log(numOfChildren); // undefined

// Null - Intentionally setting a variable to have no value

const salary = null;

console.log(salary); // null

// NON_PRIMITIVE TYPES

// Object - Storing user data

const user = {
  firstName: "Jane",
  lastName: "Doe",
  age: 30,
};

console.log(user);

// Array - Storing a list of items

const numbers = [19, "Jane", true, null, undefined, { name: "Alice" }];
